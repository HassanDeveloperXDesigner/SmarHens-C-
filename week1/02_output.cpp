# include <iostream>
using namespace std;

main(){

    // text and numbers output
    cout<<"text"<<1234<<endl;

    // output with calculation
    cout<<1+2<<endl;
    cout<<1*2<<endl;
    cout<<1/2<<endl;

    // escape sequences
    cout<<12345<<"\n\n ";
    cout<<"this is \"a\" text\n";
    cout<<"this is a text"<<endl;
    cout<<"\tthis is a text\n";

    return 0;
}